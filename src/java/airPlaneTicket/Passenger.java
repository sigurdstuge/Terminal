package airPlaneTicket;

public class Passenger{

    String name;
    String title;
    int dayOfBirth;
    double phoneNumber;
    String email;
    double bonusNumber;

    Passenger(String name, String title, int dayOfBirth, double phoneNumber, String email, double bonusNumber) {
        this.name = name;
        this.title = title;
        this.dayOfBirth = dayOfBirth;
        this.phoneNumber = phoneNumber;
        this.email = email;
        this.bonusNumber = bonusNumber;
    }

}
